import { userClient, travelClient, expenseClient } from './apiClient';

// ?? Auth Service ??????????????????????????????????????????????????????????????
export const authService = {
    register: (data) => userClient.post('/api/auth/register', data),
    login: (data) => userClient.post('/api/auth/login', data),
};

// ?? User Service ??????????????????????????????????????????????????????????????
export const userService = {
    getMe: () => userClient.get('/api/users/me'),
    getAll: () => userClient.get('/api/users'),
    getById: (id) => userClient.get(`/api/users/${id}`),
    update: (id, data) => userClient.put(`/api/users/${id}`, data),
    adminUpdate: (id, data) => userClient.put(`/api/users/${id}/admin`, data),
    delete: (id) => userClient.delete(`/api/users/${id}`),
};

// ?? TravelPlan Service ????????????????????????????????????????????????????????
export const tripService = {
    getAll: () => travelClient.get('/api/travel-plans'),
    getById: (id) => travelClient.get(`/api/travel-plans/${id}`),
    getByShareToken: (token) => travelClient.get(`/api/travel-plans/shared/${token}`),
    create: (data) => travelClient.post('/api/travel-plans', data),
    update: (id, data) => travelClient.put(`/api/travel-plans/${id}`, data),
    delete: (id) => travelClient.delete(`/api/travel-plans/${id}`),
};

